// T:O(n), S:O(1)
class Solution {
public:
    string addBinary(string a, string b) {
        int la = a.size();
        int lb = b.size();
        int i = la - 1;
        int j = lb - 1;
        int c = 0;
        string r;
        while (i >= 0 || j >= 0 || c == 1) {
            int sum = c;
            if (i >= 0) {
                sum += a[i] - '0';
                --i;
            }

            if (j >= 0) {
                sum += b[j] - '0';
                --j;
            }

            r += '0' + sum % 2;
            c = sum / 2;
        }
        reverse(r.begin(), r.end());

        return r;
    }
};

